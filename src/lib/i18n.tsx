const DICTS: Record<Locale, Dict> = { en, id, zh, ja, ko, es };

type Ctx = { locale: Locale; setLocale: (l: Locale) => void; t: (key: string) => string };
const I18nCtx = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>("en");
  useEffect(() => {
    const saved = typeof window !== "undefined" ? (localStorage.getItem("trenz.locale") as Locale | null) : null;
    if (saved && DICTS[saved]) setLocale(saved);
  }, []);
  useEffect(() => {
    if (typeof window !== "undefined") localStorage.setItem("trenz.locale", locale);
  }, [locale]);
  const value = useMemo<Ctx>(() => ({
    locale,
    setLocale,
    t: (k) => DICTS[locale][k] ?? DICTS.en[k] ?? k,
  }), [locale]);
  return <I18nCtx.Provider value={value}>{children}</I18nCtx.Provider>;
}

export function useI18n() {
  const c = useContext(I18nCtx);
  if (!c) throw new Error("useI18n must be used within I18nProvider");
  return c;
}
